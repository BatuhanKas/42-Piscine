#include <stdio.h>

void	ft_swap(int *a, int *b)
{
    char tmp;
    
    tmp = *a;
    *a = *b;
    *b = tmp;
}

int main()
{
    int a = 5;
    int b = 3;
    printf("%d, %d\n", a, b);
    ft_swap(&a, &b);
    printf("%d, %d", a, b);
}