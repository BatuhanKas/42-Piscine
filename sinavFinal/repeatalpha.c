#include <unistd.h>

int main(int argc, char **argv)
{
    int i = 0;
    int j = 0;
    int x;

    if (argc == 2)
    {
        while (argv[1][i] != '\0')
        {
            if(argv[1][i] >= 'a' && argv[1][i] <= 'z')
            {
                x = 0;
                j = argv[1][i] - 97 + 1;
                while(x < j)
                {
                    write(1, &argv[1][i], 1);
                    x++;
                }
                x = 0;
                i++;
            }
            if (argv[1][i] >= 'A' && argv[1][i] <= 'Z')
            {
                x = 0;
                j = argv[1][i] - 65 + 1;
                while(x < j)
                {
                    write(1, &argv[1][i], 1);
                    x++;
                }
                x = 0;
                i++;
            }
        }
    }
}