#include <unistd.h>

int main(int argc, char **argv)
{
    int i = 0;
    int j = 0;

    i = argc - 1;
    if(argc > 1)
    {
        if(argv[i][j])
        {
            while (argv[i][j] != '\0')
            {
                write(1, &argv[i][j], 1);
                j++;
            }
        }
    }
    write(1, "\n", 1);
    return (0);
}