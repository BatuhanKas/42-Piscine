#include <stdio.h>

char    *ft_strcpy(char *s1, char *s2)
{
    int i = 0;

    while(s2[i] != '\0'){
        s1[i] = s2[i];
        i++;
    }
    if(s2[i] == '\0')
        s1[i] = '\0';
    return (s1);
}

int main()
{
    char s1[] = "ali";
    char s2[] = "memocan";
    printf("%s", ft_strcpy(s1, s2));
}